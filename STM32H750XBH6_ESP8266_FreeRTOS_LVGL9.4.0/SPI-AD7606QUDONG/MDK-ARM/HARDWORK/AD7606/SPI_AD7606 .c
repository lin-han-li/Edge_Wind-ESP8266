/*
*********************************************************************************************************
* 函 数 名: AD7606_read_data
* 功能说明: 手动模拟SPI时序读取数据 (主要读取函数)
* 形    参: DB_data - 存储读取结果的数组指针
* 返 回 值: 无
* 说    明:
* 1. 该函数目前循环 `i<1`，似乎只读取了第1个通道的数据。
* 2. 使用 `READ_AD7606_DB7` 作为数据输入引脚 (MISO)，即AD7606的串行输出模式。
* 3. 通过16次SCLK翻转，逐位移位读取16位ADC结果。
*********************************************************************************************************
*/
void AD7606_read_data(uint16_t *DB_data)
{
	uint8_t i, j;
	AD7606_CS_L; // 拉低片选，开始SPI通信

	/* 循环读取通道数据 (目前代码只读取1次/1个通道) */
	for (i = 0; i < 1; i++)
	{
		uint16_t DB_data1 = 0;
		// delay_us(1);

		/* 读取16个Bit */
		for (j = 0; j < 16; j++)
		{
			AD7606_SCLK_L; // SCLK拉低
			delay_us(1);   // 延时，建立时钟周期

			/* 读取数据位：读取DB7引脚状态，并移位到对应位置
			   AD7606默认是高位先出 (MSB First)
			*/
			DB_data1 = ((uint16_t)(READ_AD7606_DB7) << (15 - j)) + DB_data1;

			AD7606_SCLK_H; // SCLK拉高，产生上升沿
			// delay_us(1);
		}
		DB_data[i] = (uint16_t)DB_data1; // 保存该通道数据
		// AD7606_StartConv();
	}
	AD7606_CS_H;		// 通信结束，拉高片选
	AD7606_StartConv(); // 启动下一次转换
}


/*
*********************************************************************************************************
* 函 数 名: AD7606_Init
* 功能说明: AD7606 芯片初始化流程
* 形    参: 无
* 返 回 值: 无
*********************************************************************************************************
*/
void AD7606_Init()
{
	/* 1. 配置STM32的GPIO引脚模式（推挽输出、浮空输入等） */
	// AD7606_ConfigGPIO(); // 如果此函数在其他地方定义，需取消注释

	/* 2. 初始化控制引脚的默认电平 */
	AD7606_CONVEST_A_H; // 转换开始信号A置高
	AD7606_CONVEST_B_H; // 转换开始信号B置高
	delay_us(500);		// 等待上电稳定

	// STby_Set;        // 退出待机模式（如果使用了STBY引脚）
	AD7606_SCLK_H; // SPI时钟线默认拉高（空闲态为高，CPOL=1）
	AD7606_CS_H;   // 片选信号拉高，取消选中

	/* 3. 设置过采样模式 (Oversampling) */
	/* 默认先全部拉低 */
	AD7606_OS2_L;
	AD7606_OS1_L;
	AD7606_OS0_L;
	AD7606_SetOS(3); //

	/* 4. 执行硬件复位 */
	AD7606_Reset(); // 发送复位脉冲，复位内部寄存器
	delay_us(500);	// 复位后等待芯片就绪

	/* 5. 确保CONVST引脚状态正确，准备开始转换 */
	AD7606_CONVEST_A_H;
	AD7606_CONVEST_B_H;

	/* 6. 开启第一次转换，让芯片进入工作状态 */
	AD7606_StartConv();
}

/*
*********************************************************************************************************
* 函 数 名: AD7606_SetOS
* 功能说明: 设置过采样模式（数字滤波，硬件求平均值)
* 形    参: _ucMode : 0-6
*
* 【AD7606 过采样模式参数表】
* | _ucMode | 过采样倍数 | 转换时间(t_conv) | 理论最高采样率 |
* |---------|------------|------------------|----------------|
* |    0    |   No OS    |      4 μs        |    200 kSPS    |
* |    1    |     2      |    8.7 μs        |    100 kSPS    |
* |    2    |     4      |   17.5 μs        |     50 kSPS    |
* |    3    |     8      |     35 μs        |     25 kSPS    |
* |    4    |    16      |     70 μs        |   12.5 kSPS    |
* |    5    |    32      |    140 μs        |   6.25 kSPS    | <-- 您当前的设置，需等待至少140us
* |    6    |    64      |    280 μs        |  3.125 kSPS    |
*
* 返 回 值: 无
* 说    明: 通过控制 OS2, OS1, OS0 三个引脚的高低电平组合来选择模式
*********************************************************************************************************
*/
void AD7606_SetOS(uint8_t _ucMode)
{
	if (_ucMode == 1) // 2倍
	{
		AD7606_OS2_L;
		AD7606_OS1_L;
		AD7606_OS0_H;
	}
	else if (_ucMode == 2) // 4倍
	{
		AD7606_OS2_L;
		AD7606_OS1_H;
		AD7606_OS0_L;
	}
	else if (_ucMode == 3) // 8倍
	{
		AD7606_OS2_L;
		AD7606_OS1_H;
		AD7606_OS0_H;
	}
	else if (_ucMode == 4) // 16倍
	{
		AD7606_OS2_H;
		AD7606_OS1_L;
		AD7606_OS0_L;
	}
	else if (_ucMode == 5) // 32倍
	{
		AD7606_OS2_H;
		AD7606_OS1_L;
		AD7606_OS0_H;
	}
	else if (_ucMode == 6) // 64倍
	{
		AD7606_OS2_H;
		AD7606_OS1_H;
		AD7606_OS0_L;
	}
	else /* 默认按0处理：无过采样 */
	{
		AD7606_OS2_L;
		AD7606_OS1_L;
		AD7606_OS0_L;
	}
}

/*
*********************************************************************************************************
* 函 数 名: AD7606_Reset
* 功能说明: 硬件复位操作
* 形    参: 无
* 返 回 值: 无
* 说    明: AD7606复位引脚为高电平有效 (Active High)。
* 时序要求：RESET高电平脉冲宽度至少为50ns。
*********************************************************************************************************
*/
void AD7606_Reset(void)
{
	/* 复位期间，建议将通信引脚置于空闲状态 */
	AD7606_CS_H;
	AD7606_SCLK_H;

	/* 产生复位脉冲：低 -> 高 -> 低 */
	AD7606_REST_L;
	AD7606_REST_L; // 稍微延时确保低电平

	AD7606_REST_H; // 拉高RESET，开始复位
	AD7606_REST_H; // 保持高电平，确保满足最小脉宽要求(>50ns)
	AD7606_REST_H;
	AD7606_REST_H;

	AD7606_REST_L; // 拉低RESET，结束复位，芯片开始工作
	AD7606_REST_L;
}

